package cn.hrbeu.po;

import java.util.Date;

public class Goods {
    private Integer goodsId;

    private String goodsName;

    private Double goodsPrice;

    private Date goodsFactorydate;

    private Double goodsRepairtime;

    private String goodsColor;

    private String goodsType;

    private Double goodsOffprice;

    private String goodsPicture;
    
    private String goodsHasSelled;
    
    public Integer getGoodsId() {
		return goodsId;
	}

	public void setGoodsId(Integer goodsId) {
		this.goodsId = goodsId;
	}

	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	public Double getGoodsPrice() {
		return goodsPrice;
	}

	public void setGoodsPrice(Double goodsPrice) {
		this.goodsPrice = goodsPrice;
	}

	public Date getGoodsFactorydate() {
		return goodsFactorydate;
	}

	public void setGoodsFactorydate(Date goodsFactorydate) {
		this.goodsFactorydate = goodsFactorydate;
	}



	public String getGoodsColor() {
		return goodsColor;
	}

	public void setGoodsColor(String goodsColor) {
		this.goodsColor = goodsColor;
	}

	public String getGoodsType() {
		return goodsType;
	}

	public void setGoodsType(String goodsType) {
		this.goodsType = goodsType;
	}

	public Double getGoodsOffprice() {
		return goodsOffprice;
	}

	public void setGoodsOffprice(Double goodsOffprice) {
		this.goodsOffprice = goodsOffprice;
	}

	public String getGoodsPicture() {
		return goodsPicture;
	}

	public void setGoodsPicture(String goodsPicture) {
		this.goodsPicture = goodsPicture;
	}

	public Double getGoodsRepairtime() {
		return goodsRepairtime;
	}

	public void setGoodsRepairtime(Double goodsRepairtime) {
		this.goodsRepairtime = goodsRepairtime;
	}

	public String getGoodsHasSelled() {
		return goodsHasSelled;
	}

	public void setGoodsHasSelled(String goodsHasSelled) {
		this.goodsHasSelled = goodsHasSelled;
	}

	

    
}